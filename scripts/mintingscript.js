const rpcEndPoint = {
    test: 'https://matic-mumbai.chainstacklabs.com',
    main: 'https://maticnode1.anyswap.exchange'
}

/******* To be changed for mainnet */
const contractAddress = '0x4f270F7fA4E9131ff4dC540BBbDa48e1E46dfF20'
const provider = new Web3.providers.HttpProvider(rpcEndPoint.test)
/******************************** */

const w3 = new Web3(provider)

let run = async (acc) => {
    let collection = new w3.eth.Contract(abi,contractAddress)
    
    let availMinting = await collection.methods['mintingLeftInEpoch']().call()
    let mintingAvailable = (availMinting>0)
    let initial = availMinting;
    while(mintingAvailable){
        let current = initial-availMinting + 1
        console.log('Minting ' + current + ' of ' + initial)
        let gasPrice = await w3.eth.getGasPrice()
        let func = collection.methods['mint']()
        let gas = 2000000
        try{
            gas = await func.estimateGas({from: acc.address, gasPrice})
        } catch (e){
            console.log(e)
        }
        let encABI = func.encodeABI()
        let txCount = await w3.eth.getTransactionCount(acc.address)
        
        let txObject = {
            nonce:    w3.utils.toHex(txCount),
            to:       contractAddress,
            gasLimit: w3.utils.toHex(gas),
            gasPrice: w3.utils.toHex(gasPrice),
            data: encABI
        }
        

        const signedTx = await w3.eth.accounts.signTransaction(txObject, acc.privateKey)
        try{
            await w3.eth.sendSignedTransaction(signedTx.rawTransaction)
        }
        catch(e){
            console.log(e)
        }

        availMinting = await collection.methods['mintingLeftInEpoch']().call()
        mintingAvailable = (availMinting>0)
    }
    return 'done'
}

let acc = w3.eth.accounts.privateKeyToAccount('[PRIVATE_KEY]')
run(acc).then(console.log)

  
